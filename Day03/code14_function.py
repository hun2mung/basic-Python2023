# 함수

# 함수 정의 - 실행 x
def add(x, y):  
    result = x + y
    return result

def sub(x, y):
    result = x - y
    return result

def mul(x, y):
    result = x * y
    return result

def div(x, y):
    result = x // y
    return result

# 함수 호출
val = add(1024, 5)
print(val)

val = sub(1024, 100)
print(val)

val = mul(512, 2)
print(val)

val = div(108, 10)
print(val)